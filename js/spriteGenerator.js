function generateSprites() {
    let eGV = vars.enemies.spritesGenerated;
    for (spriteName in eGV) {
        if (spriteName!=='empty') {
            let data = eGV[spriteName];
            scene.textures.generate('enemySpriteGen_' + spriteName, { data: data, pixelWidth: 80*vars.game.scale, palette: vars.enemies.palette });
        }
    }
}